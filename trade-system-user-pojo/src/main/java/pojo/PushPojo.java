package pojo;

public class PushPojo {

    private String nickid;
    private String zjSwitch;
    private String zhSwitch;
    private String addDate;
    private String updateDate;

    public String getNickid() {
        return nickid;
    }

    public void setNickid(String nickid) {
        this.nickid = nickid;
    }

    public String getZjSwitch() {
        return zjSwitch;
    }

    public void setZjSwitch(String zjSwitch) {
        this.zjSwitch = zjSwitch;
    }

    public String getZhSwitch() {
        return zhSwitch;
    }

    public void setZhSwitch(String zhSwitch) {
        this.zhSwitch = zhSwitch;
    }

    public String getAddDate() {
        return addDate;
    }

    public void setAddDate(String addDate) {
        this.addDate = addDate;
    }

    public String getUpdateDate() {
        return updateDate;
    }

    public void setUpdateDate(String updateDate) {
        this.updateDate = updateDate;
    }
}
