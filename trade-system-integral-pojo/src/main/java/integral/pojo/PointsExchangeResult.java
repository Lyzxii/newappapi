package integral.pojo;


public class PointsExchangeResult {
    private String cnickid;
    private String ext_goods_name;
    private String result;
    private String desc;

    public String getCnickid() {
        return cnickid;
    }

    public void setCnickid(String cnickid) {
        this.cnickid = cnickid;
    }

    public String getExt_goods_name() {
        return ext_goods_name;
    }

    public void setExt_goods_name(String ext_goods_name) {
        this.ext_goods_name = ext_goods_name;
    }

    public String getResult() {
        return result;
    }

    public void setResult(String result) {
        this.result = result;
    }

    public String getDesc() {
        return desc;
    }

    public void setDesc(String desc) {
        this.desc = desc;
    }
}
